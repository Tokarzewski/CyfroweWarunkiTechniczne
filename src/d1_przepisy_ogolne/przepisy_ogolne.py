def typ_mieszkalny(typ: str) -> bool:
    """Funkcja pomocnicza nie wynikają wprost z przepisów.
        Dostępne typy mieszkalne: zabudowa_jednorodzinna,
        budynek_mieszkalny, budynek_zamieszkania_zbiorowego,
        mieszkanie, pomieszczenie_mieszkalne.
    """
    typy_mieszkalne = ["zabudowa_jednorodzinna",
                       "budynek_mieszkalny", 
                       "budynek_zamieszkania_zbiorowego",
                       "mieszkanie",
                       "pomieszczenie_mieszkalne"
                       ]
    if typ in typy_mieszkalne:
        return True
    else:
        return False


def grupa_wysokosci(typ_mieszkalny: bool, wysokosc: float, kondygnacja_nadziemna: int) -> str:
    """d1.p8 - W celu określenia wymagań technicznych i użytkowych 
    wprowadza się następujący podział budynków na grupy wysokości:
    niskie (N), średniowysokie (SW), wysokie (W), wysokościowe (WW).
    """
    if wysokosc > 55:
        return "WW"
    if typ_mieszkalny:
        if kondygnacja_nadziemna > 9 or wysokosc > 25:
            return "W"
        elif kondygnacja_nadziemna > 4 or wysokosc > 12:
            return "SW"
        else:
            return "N"
    else:
        if wysokosc > 25:
            return "W"
        elif wysokosc > 12:
            return "SW"
        else:
            return "N"